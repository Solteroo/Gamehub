let CUR_LANG = localStorage.getItem('gh_lang') || 'en';

function t(key){
  return (I18N[CUR_LANG] && I18N[CUR_LANG][key]) || I18N.en[key] || key;
}

function applyLang(lang){
  CUR_LANG = lang;
  localStorage.setItem('gh_lang', lang);
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = t(el.dataset.i18n); });
  document.querySelectorAll('[data-i18n-html]').forEach(el => { el.innerHTML = t(el.dataset.i18nHtml); });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => { el.placeholder = t(el.dataset.i18nPh); });

  // dropdown active state + trigger label/flag
  const flags = {uz:"🇺🇿", en:"🇬🇧", ru:"🇷🇺", tk:"🇹🇲"};
  const codes = {uz:"UZ", en:"EN", ru:"RU", tk:"TK"};
  document.querySelectorAll('#langList button').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === lang);
  });
  const f = document.getElementById('ltFlag');
  const c = document.getElementById('ltCode');
  if(f) f.textContent = flags[lang] || "🌐";
  if(c) c.textContent = codes[lang] || lang.toUpperCase();

  if(window.render) window.render();
}
